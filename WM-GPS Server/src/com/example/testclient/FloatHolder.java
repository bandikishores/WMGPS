package com.example.testclient;

public class FloatHolder {
	float value = 0;

	public FloatHolder(float value) {
		super();
		this.value = value;
	}

	public FloatHolder() {
		super();
	}

	public float getValue() {
		return value;
	}

	public void setValue(float value) {
		this.value = value;
	};
	
	

}
